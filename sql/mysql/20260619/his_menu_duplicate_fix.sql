-- ====================================================================
-- HIS 菜单重复和路径修复脚本
-- 修复重复菜单、缺失组件、路径不一致问题
-- 执行时间: 2026-06-19
-- ====================================================================

USE `yudao-ai-his`;

-- ====================================================================
-- 问题分析
-- ====================================================================
-- 1. 重复菜单（完全相同的菜单定义）
--    - 预交金管理: 6768, 6850 (相同 parent_id, path, component)
--    - 床位管理: 6748, 6764 (相同 parent_id, path, component)
--    - 入院管理/入院登记: 6747, 6763 (相同 parent_id, path, component)
--
-- 2. 重复名称但不同模块（需要保留，但区分名称）
--    - 病区管理: 6762(基础数据), 6765(住院管理) - 需要改名
--    - 护理记录: 6750(住院管理), 6859(医嘱执行) - 位置不同，可保留
--    - 医嘱管理: 6749(住院管理), 6856(医嘱执行) - 位置不同，可保留
--
-- 3. 医嘱执行(6855) 问题
--    - 类型是「菜单」但没有组件路径
--    - 应该改为「目录」类型
--
-- 4. 医嘱执行子菜单组件路径错误
--    - his/order/index 应为 his/order-exec/order/index
--    - his/medication-admin/index 应为 his/order-exec/medication-admin/index
--    - 等等

-- ====================================================================
-- 修复操作
-- ====================================================================

-- --------------------------------------------------------------------
-- 1. 删除完全重复的菜单（保留 ID 较小的）
-- --------------------------------------------------------------------

-- 删除重复的预交金管理 (6850)
DELETE FROM `system_menu` WHERE `id` = 6850;

-- 删除重复的床位管理 (6764)
DELETE FROM `system_menu` WHERE `id` = 6764;

-- 删除重复的入院登记 (6763) - 与入院管理完全相同
DELETE FROM `system_menu` WHERE `id` = 6763;

-- 删除医嘱执行下的按钮 (6863) - 这是一个按钮类型，不应该叫「医嘱执行」
DELETE FROM `system_menu` WHERE `id` = 6863;

-- 删除孤立按钮（父菜单 6850 已删除）
DELETE FROM `system_menu` WHERE `id` = 6851;  -- 预交金查询
DELETE FROM `system_menu` WHERE `id` = 6853;  -- 预交金退款
DELETE FROM `system_menu` WHERE `id` = 6854;  -- 预交金删除

-- --------------------------------------------------------------------
-- 2. 修复病区管理重名问题
-- --------------------------------------------------------------------

-- 6765 在住院管理下，改名为「病区设置」更合适
UPDATE `system_menu`
SET `name` = '病区设置'
WHERE `id` = 6765;

-- --------------------------------------------------------------------
-- 3. 修复医嘱执行(6855) - 改为目录类型
-- --------------------------------------------------------------------

UPDATE `system_menu`
SET `type` = 1  -- 1=目录
WHERE `id` = 6855;

-- --------------------------------------------------------------------
-- 4. 修复医嘱执行子菜单的组件路径
-- --------------------------------------------------------------------

-- 医嘱管理
UPDATE `system_menu`
SET `component` = 'his/order-exec/order/index'
WHERE `id` = 6856;

-- 给药记录
UPDATE `system_menu`
SET `component` = 'his/order-exec/medication-admin/index'
WHERE `id` = 6857;

-- 生命体征
UPDATE `system_menu`
SET `component` = 'his/order-exec/vital-sign/index'
WHERE `id` = 6858;

-- 护理记录
UPDATE `system_menu`
SET `component` = 'his/order-exec/nursing-record/index'
WHERE `id` = 6859;

-- 护理评估
UPDATE `system_menu`
SET `component` = 'his/order-exec/nursing-assessment/index'
WHERE `id` = 6860;

-- ====================================================================
-- 验证结果
-- ====================================================================

-- 检查是否还有重复名称
SELECT
    name AS '菜单名称',
    COUNT(*) AS '数量',
    GROUP_CONCAT(id ORDER BY id) AS 'ID列表'
FROM `system_menu`
WHERE `deleted` = 0 AND `status` = 0
GROUP BY `name`
HAVING COUNT(*) > 1;

-- 查看修复后的医嘱执行模块
SELECT
    id AS 'ID',
    parent_id AS '父ID',
    name AS '名称',
    CASE type WHEN 1 THEN '目录' WHEN 2 THEN '菜单' WHEN 3 THEN '按钮' END AS '类型',
    path AS '路径',
    component AS '组件'
FROM `system_menu`
WHERE (`id` = 6855 OR `parent_id` = 6855)
  AND `deleted` = 0
ORDER BY `id`;

