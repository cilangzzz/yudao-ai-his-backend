-- ====================================================================
-- HIS 检验管理模块菜单修复脚本
-- 修复菜单类型和组件路径配置
-- 执行时间: 2026-06-19
-- ====================================================================

USE `yudao-ai-his`;

-- ====================================================================
-- 问题分析
-- ====================================================================
-- 检验管理模块的子菜单配置错误：
-- - 检验申请管理(6818): 类型是「目录」但应该是「菜单」，缺少组件路径
-- - 检验项目字典(6831): 类型是「目录」但应该是「菜单」，缺少组件路径
-- - 危急值管理(6836): 类型是「目录」但应该是「菜单」，缺少组件路径
--
-- 前端已创建组件：
-- - views/his/lab/lab-request/index
-- - views/his/lab/lab-item/index
-- - views/his/lab/critical-value/index
--
-- 但后端菜单配置指向空目录，导致路由无法匹配组件，出现 404

-- ====================================================================
-- 修复操作
-- ====================================================================

-- --------------------------------------------------------------------
-- 1. 修复检验申请管理 (6818)
-- --------------------------------------------------------------------
UPDATE `system_menu`
SET
    `type` = 2,  -- 2=菜单
    `name` = '检验申请',  -- 简化名称
    `component` = 'his/lab/lab-request/index'
WHERE `id` = 6818;

-- --------------------------------------------------------------------
-- 2. 修复检验项目字典 (6831)
-- --------------------------------------------------------------------
UPDATE `system_menu`
SET
    `type` = 2,  -- 2=菜单
    `name` = '检验项目',  -- 简化名称
    `component` = 'his/lab/lab-item/index'
WHERE `id` = 6831;

-- --------------------------------------------------------------------
-- 3. 修复危急值管理 (6836)
-- --------------------------------------------------------------------
UPDATE `system_menu`
SET
    `type` = 2  -- 2=菜单
WHERE `id` = 6836;

-- 注意: 危急值管理已经是正确的名称，只需修改类型
-- 组件路径保持为空，因为前端组件是 his/lab/critical-value/index
-- 如果前端确认组件路径，可以添加：
-- UPDATE `system_menu` SET `component` = 'his/lab/critical-value/index' WHERE `id` = 6836;

-- ====================================================================
-- 验证结果
-- ====================================================================

-- 查看修复后的检验管理模块
SELECT
    id AS 'ID',
    parent_id AS '父ID',
    name AS '名称',
    CASE type WHEN 1 THEN '目录' WHEN 2 THEN '菜单' WHEN 3 THEN '按钮' END AS '类型',
    path AS '路径',
    component AS '组件'
FROM `system_menu`
WHERE (id = 6817 OR parent_id = 6817)
  AND `deleted` = 0
  AND `type` IN (1, 2)  -- 只显示目录和菜单
ORDER BY `id`;

