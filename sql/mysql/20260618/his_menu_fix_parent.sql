-- ====================================================================
-- HIS 菜单归属修复脚本
-- 修复检验管理和医嘱执行菜单的 parent_id
-- 执行时间: 2026-06-18
-- ====================================================================

USE `yudao-ai-his`;

-- ====================================================================
-- 问题说明
-- ====================================================================
-- 检验管理 (id=6817) 和 医嘱执行 (id=6855) 的 parent_id 为 0
-- 导致它们显示为顶级菜单，而不是在 HIS 医院信息系统下
-- 需要将 parent_id 改为 6735 (HIS医院信息系统)

-- ====================================================================
-- 修复操作
-- ====================================================================

-- 1. 将检验管理移到 HIS 医院信息系统下
UPDATE `system_menu`
SET `parent_id` = 6735
WHERE `id` = 6817;

-- 2. 将医嘱执行移到 HIS 医院信息系统下
UPDATE `system_menu`
SET `parent_id` = 6735
WHERE `id` = 6855;

-- ====================================================================
-- 验证结果
-- ====================================================================

-- 查看 HIS 医院信息系统的直接子菜单
SELECT
    id AS 'ID',
    name AS '菜单名称',
    CASE type WHEN 1 THEN '目录' WHEN 2 THEN '菜单' WHEN 3 THEN '按钮' END AS '类型',
    path AS '路径'
FROM `system_menu`
WHERE `parent_id` = 6735
  AND `deleted` = 0
  AND `status` = 0
ORDER BY `id`;

-- 查看顶级菜单（应该只有系统管理、基础设施、HIS医院信息系统）
SELECT
    id AS 'ID',
    name AS '菜单名称',
    path AS '路径'
FROM `system_menu`
WHERE `parent_id` = 0
  AND `deleted` = 0
  AND `status` = 0
ORDER BY `id`;

